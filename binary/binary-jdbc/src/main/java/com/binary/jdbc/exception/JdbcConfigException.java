package com.binary.jdbc.exception;

public class JdbcConfigException extends JdbcException {
	private static final long serialVersionUID = 1L;
	
	
	public JdbcConfigException() {
		super();
	}
	
	public JdbcConfigException(String message) {
		super(message);
	}
	
	public JdbcConfigException(Throwable cause) {
		super(cause);
	}
	
	public JdbcConfigException(String message, Throwable cause) {
		super(message, cause);
	}
	
	
	
	
}
