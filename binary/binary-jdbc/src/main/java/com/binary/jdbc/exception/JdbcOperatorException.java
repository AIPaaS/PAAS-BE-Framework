package com.binary.jdbc.exception;

public class JdbcOperatorException extends JdbcException {
	private static final long serialVersionUID = 1L;
	
	
	public JdbcOperatorException() {
		super();
	}
	
	public JdbcOperatorException(String message) {
		super(message);
	}
	
	public JdbcOperatorException(Throwable cause) {
		super(cause);
	}
	
	public JdbcOperatorException(String message, Throwable cause) {
		super(message, cause);
	}
	
	
	
	
}
